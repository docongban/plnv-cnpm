package enity;

/**
 *
 * @author docon
 */
public class SalaryUpdate {
    private int id;
    private double salary;

    public SalaryUpdate() {
        
    }

    public SalaryUpdate(int id, double salary) {
        this.id = id;
        this.salary = salary;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }
    
    
}
